# Calculate simple interest.

priciple_amount = float(input("Enter your Amount : "))
rate_intrest = float(input("Enter Rate of Intrest : "))
time = int(input("Enter time in years : "))

simple_intrest = (priciple_amount+rate_intrest+time)/100

print("Total Amount = ",priciple_amount)
print("Simple Intrest = ",simple_intrest)
